public class GameEntity {
    protected int healthPointBoss;
    protected int damageBoss;
    protected String defenseBoss;

    public int getHealthPointBoss() {
        return healthPointBoss;
    }

    public void setHealthPointBoss(int healthPointBoss) {
        this.healthPointBoss = healthPointBoss;
    }

    public int getDamageBoss() {
        return damageBoss;
    }

    public void setDamageBoss(int damageBoss) {
        this.damageBoss = damageBoss;
    }

    public String getDefenseBoss() {
        return defenseBoss;
    }

    public void setDefenseBoss(String defenseBoss) {
        this.defenseBoss = defenseBoss;
    }

    public int getHealthPointHeroes() {
        return healthPointHeroes;
    }

    public void setHealthPointHeroes(int healthPointHeroes) {
        this.healthPointHeroes = healthPointHeroes;
    }

    public int getDamageHeroes() {
        return damageHeroes;
    }

    public void setDamageHeroes(int damageHeroes) {
        this.damageHeroes = damageHeroes;
    }

    public String getDefenseHeroes() {
        return defenseHeroes;
    }

    public void setDefenseHeroes(String defenseHeroes) {
        this.defenseHeroes = defenseHeroes;
    }

    protected int healthPointHeroes;
    protected int damageHeroes;
    protected String defenseHeroes;
}
